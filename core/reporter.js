/**
 * Sends a compliance report email with PDF attachment using SES.
 */
export async function sendReport(recipientEmail, clientName, reportHtml, pdfBuffer = null) {
    if (!process.env.PLATFORM_AWS_ACCESS_KEY_ID && !process.env.AWS_LAMBDA_FUNCTION_NAME) {
        console.warn('[REPORTER] Skipping email send — missing credentials.');
        return { sent: false, reason: 'no_credentials' };
    }

    const transporter = nodemailer.createTransport({
        SES: { ses: sesClient, aws: { SendRawEmailCommand } }
    });

    const mailOptions = {
        from: FROM_EMAIL,
        to: recipientEmail,
        subject: `ComplianceFlow AI — Audit Report: ${clientName}`,
        html: reportHtml,
    };

    if (pdfBuffer) {
        mailOptions.attachments = [{
            filename: `ComplianceFlow_Report_${clientName.replace(/\s+/g, '_')}.pdf`,
            content: pdfBuffer,
            contentType: 'application/pdf'
        }];
    }

    try {
        const info = await transporter.sendMail(mailOptions);
        console.log(`[REPORTER] ✓ Report sent to ${recipientEmail} (MessageId: ${info.messageId})`);
        return { sent: true, messageId: info.messageId };
    } catch (err) {
        console.error(`[REPORTER] ❌ Failed to send report:`, err.message);
        return { sent: false, reason: err.message };
    }
}
