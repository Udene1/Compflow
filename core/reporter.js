import { SESClient, SendRawEmailCommand } from '@aws-sdk/client-ses';
import nodemailer from 'nodemailer';
import PDFDocument from 'pdfkit';
import { ControlMatrix } from './controls.js';
import { Stream } from 'stream';

const sesConfig = {
    region: process.env.AWS_REGION || 'us-east-1'
};

if (process.env.PLATFORM_AWS_ACCESS_KEY_ID && process.env.PLATFORM_AWS_SECRET_ACCESS_KEY) {
    sesConfig.credentials = {
        accessKeyId: process.env.PLATFORM_AWS_ACCESS_KEY_ID,
        secretAccessKey: process.env.PLATFORM_AWS_SECRET_ACCESS_KEY,
    };
}

const sesClient = new SESClient(sesConfig);
const FROM_EMAIL = process.env.AWS_SES_FROM_EMAIL || 'reports@complianceflow.ai';

/**
 * Maps finding text back to a ControlMatrix key for enriched metadata.
 */
function getMetadataForFinding(finding) {
    // Simple heuristic mapping
    if (finding.issue.includes('Public access')) return ControlMatrix.S3_PUBLIC;
    if (finding.issue.includes('Versioning')) return ControlMatrix.S3_VERSIONING;
    if (finding.issue.includes('Encryption')) return ControlMatrix.S3_ENCRYPTION;
    if (finding.issue.includes('MFA')) return ControlMatrix.IAM_ROOT_MFA;
    if (finding.issue.includes('Rotation')) return ControlMatrix.IAM_ACCESS_KEY_ROTATION;
    if (finding.issue.includes('Port 22')) return ControlMatrix.SG_OPEN_SSH;
    if (finding.issue.includes('Flow Logs')) return ControlMatrix.VPC_FLOW_LOGS;
    if (finding.issue.includes('Backup')) return ControlMatrix.RDS_BACKUP;
    
    return null;
}

/**
 * Generates an executive PDF report.
 */
export async function generatePdfReport(clientName, scanResults) {
    return new Promise((resolve, reject) => {
        const doc = new PDFDocument({ margin: 50 });
        const buffers = [];
        doc.on('data', buffers.push.bind(buffers));
        doc.on('end', () => resolve(Buffer.concat(buffers)));

        // --- Header Section ---
        doc.fillColor('#0f172a').rect(0, 0, 612, 80).fill();
        doc.fillColor('#ffffff').fontSize(24).text('ComplianceFlow', 50, 25, { continued: true }).fillColor('#818cf8').text('AI');
        doc.fillColor('#94a3b8').fontSize(10).text('EXECUTIVE PRIVILEGED REPORT', 400, 35);
        
        doc.moveDown(4);
        
        // --- Summary Section ---
        doc.fillColor('#000000').fontSize(20).text(`Compliance Posture: ${clientName}`, { underline: true });
        doc.fontSize(12).text(`Date: ${new Date().toLocaleDateString()}`);
        doc.moveDown();

        const total = scanResults.length;
        const failed = scanResults.filter(r => r.severity !== 'pass').length;
        const score = total > 0 ? (((total - failed) / total) * 100).toFixed(1) : '100.0';

        doc.fontSize(14).text('Executive Summary', { bold: true });
        doc.fontSize(11).text(`Current score: ${score}% across ${total} monitored controls.`);
        doc.text(`Identified ${failed} active security anomalies requiring attention.`);
        doc.moveDown();

        // --- Critical Path / Business Risk ---
        doc.fontSize(14).text('Critical Findings & Business Risk', { bold: true });
        doc.moveDown(0.5);

        const anomalies = scanResults.filter(r => r.severity !== 'pass');
        
        if (anomalies.length === 0) {
            doc.fillColor('#10b981').fontSize(11).text('✓ No critical anomalies detected. All baseline controls are within policy.');
        } else {
            anomalies.forEach((a, index) => {
                const meta = getMetadataForFinding(a);
                doc.fillColor('#000000').fontSize(11).text(`${index + 1}. ${a.name} (${a.type})`, { bold: true });
                doc.fillColor('#ef4444').fontSize(10).text(`ISSUE: ${a.issue}`);
                
                if (meta) {
                    doc.fillColor('#334155').fontSize(10).text(`BUSINESS IMPACT: ${meta.impact}`, { italic: true });
                    doc.fillColor('#0f172a').fontSize(10).text(`REMEDIATION: ${meta.remediation}`);
                }
                doc.moveDown(0.5);
            });
        }

        // --- Footer ---
        doc.fontSize(8).fillColor('#94a3b8').text('Confidential. Generated by ComplianceFlow AI Autonomous Engine.', 50, 750, { align: 'center' });

        doc.end();
    });
}

